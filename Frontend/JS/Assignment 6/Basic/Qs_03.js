// How do you access object properties using dot notation?

// Ans. --
/* by writing:- objectName.propertyName

Example: 
    let student = {
        name: 'prince',
        age: 23,
        course: 'mern'
    }

    console.log(student.name) // prince
    console.log(student.age) // 22
    console.log(student.course) // mern

Note: Dot notation works when the property name is a valid identifier (no spaces, no special characters).
*/
// What is the difference between primitive values and object values?

// Ans. ---
/* primitive values and object values differ mainly in how they store data and how assignment/copying works.

** Primitive values - Primitive values store the actual value directly.
    Primitive types in JavaScript -> string, number, boolean, null, undefined, bigint, symbol.

** Object values - Objects store a reference (memory address), not the actual data directly.
    Object types -> Object, Array, Function.

** Key difference -
    Primitive	                Object
    Stored by value	            Stored by reference
    Independent copy	        Shared reference
    Immutable (value itself)	Mutable
*/

// How do you access object properties using bracket notation?

// Ans. --
/* by writing the property name inside square brackets as a string.

Example: objectName["propertyName"]
    let student = {
        name: 'prince',
        age: 23,
        course: 'mern'
    }

    console.log(student["name"]) // prince
    console.log(student["age"]) // 23
 
Note: -> Dot notation → simple property names
    -> Bracket notation → dynamic / special property names / have spaces / come from variables
*/

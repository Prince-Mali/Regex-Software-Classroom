// What is an object in JavaScript? 

// Ans. --
/* An Object in javascript is a data type used to store collections of data in key-value pairs.

Example: A car has properties like color, brand, and speed.
    In JavaScript, that looks like this:
    let obj = {
        brand: "Toyota",
        color: "Red",
        speed: 120
    }

    Here:
        -> brand, color, and speed → keys (properties)
        -> "Toyota", "Red", 120 → values
*/

// What is the difference between dot and bracket notation?

// Ans. --
/*  1. Dot Notation -
        Dot notation is used by writing the object name followed by a dot (.) and the property name.
        It is simple, readable, and commonly used when the property name is a valid identifier.

    2. Bracket Notation -
        Bracket notation is used by writing the property name inside square brackets [] as a string.
        It is useful when the property name contains spaces, special characters, or when the property name is stored in a variable.
    
    Main Difference -
        Dot notation works only with fixed and valid property names.
        Bracket notation is more flexible and supports dynamic property access.
*/
